export const getFortuneIdByNumber = (n) => luckNumberMap[n] || null;

export const getFortuneByNumber = (n) => {
  const id = getFortuneIdByNumber(n);
  return fortunes.find(f => f.id === id) || null;
};

export const rollFortune = () => {
  const n = Math.floor(Math.random() * 100) + 1;
  const id = getFortuneIdByNumber(n);
  const fortune = fortunes.find(f => f.id === id) || null;
  return { n, id, fortune };
};
